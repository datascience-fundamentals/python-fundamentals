# Single line comments -> # character is used to start a comment
# Mutilen comments -> Python will ignore string literals that
# are not assigned to a variable, so nulti-line strings (created by
# surrounding text with triple quotes """) can be used as de facto comments.

# this is a single line comment

"""
  This is a multiple line comment
"""

# print -> It allows you to print a message
print("Hola Mundo!")

# you can print more than one value
# those values will be printed separated by space
print("Hola", "mike", "guasausky", "!")  # Hola mike guasausky !
